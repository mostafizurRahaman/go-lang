package main

import "fmt"

func main() {

	// int: 
	fmt.Println(1)
	fmt.Println(1+1)

	// string: 
	fmt.Println("hello go lang!")

	// Bool: 
	fmt.Println(true)
	fmt.Println(false)

	// Floating value: 
	fmt.Println(2.0 + 3.0)
	fmt.Println(10.5)

	// Arethmatic operation : 
	fmt.Println(3*5)
	fmt.Println(3.0/5.0)
}